# This program shows the distance a car has traveled when going 70 mph.
speed = 70
distanceAfter6 = 6
distanceAfter10 = 10
distanceAfter15 = 15

#Speed of the car
print("A car is traveling at 70 mph")

#The distance the car will travel in 6 hours.
print("distance the car will travel in 6 hours is", speed * distanceAfter6, "miles")

#The distance the car will travel in 10 hours.
print("distance the car will travel in 10 hours is", speed * distanceAfter10, "miles")

#The distance the car will travel in 15 hours.
print("distance the car will travel in 15 hours is", speed * distanceAfter15, "miles")

